namespace MartignaniDaniele.utils
{
    // Provides constant values for different attributes of bloons in the tower defense game.
    // The BloonValues class contains static fields for health, speed, and money associated with various types of bloons.
    public static class BloonValues
    {
        // The default health value for a red bloon.
        public const int RedBloonHealth = 40;

        // The default speed value for a red bloon.
        public const double RedBloonSpeed = 10;

        // The default money value gained when a red bloon is defeated.
        public const int RedBloonMoney = 30;

        // The default health value for a blue bloon.
        public const int BlueBloonHealth = 70;

        // The default speed value for a blue bloon.
        public const double BlueBloonSpeed = 20;

        // The default money value gained when a blue bloon is defeated.
        public const int BlueBloonMoney = 30;

        // The default health value for a black bloon.
        public const int BlackBloonHealth = 80;

        // The default speed value for a black bloon.
        public const double BlackBloonSpeed = 30;

        // The default money value gained when a black bloon is defeated.
        public const int BlackBloonMoney = 30;
    }
}